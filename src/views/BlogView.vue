      <section aria-labelledby="values"><h3 id="values"><a href="#values">Values</a></h3>
        <div class="section-content">
          <dl>
            <dt id="static"><code>static</code></dt>
            <dd>
              <p>The element is positioned according to the normal flow of the document. The <a
                  href="/en-US/docs/Web/CSS/top"><code>top</code></a>, <a
                  href="/en-US/docs/Web/CSS/right"><code>right</code></a>, <a href="/en-US/docs/Web/CSS/bottom"><code>bottom</code></a>,
                <a href="/en-US/docs/Web/CSS/left"><code>left</code></a>, and <a href="/en-US/docs/Web/CSS/z-index"><code>z-index</code></a>
                properties have <em>no effect</em>. This is the default value.</p>
            </dd>
            <dt id="relative"><code>relative</code></dt>
            <dd>
              <p>The element is positioned according to the normal flow of the document, and then offset <em>relative to
                itself</em> based on the values of <code>top</code>, <code>right</code>, <code>bottom</code>, and <code>left</code>.
                The offset does not affect the position of any other elements; thus, the space given for the element in
                the page layout is the same as if position were <code>static</code>.</p>
              <p>This value creates a new <a
                  href="/en-US/docs/Web/CSS/CSS_Positioning/Understanding_z_index/The_stacking_context">stacking
                context</a> when the value of <code>z-index</code> is not <code>auto</code>. Its effect on <code>table-*-group</code>,
                <code>table-row</code>, <code>table-column</code>, <code>table-cell</code>, and <code>table-caption</code>
                elements is undefined.</p>
            </dd>
            <dt id="absolute"><code>absolute</code></dt>
            <dd>
              <p>The element is removed from the normal document flow, and no space is created for the element in the page
                layout. It is positioned relative to its closest <a href="#types_of_positioning">positioned</a> ancestor,
                if any; otherwise, it is placed relative to the initial <a href="/en-US/docs/Web/CSS/Containing_block">containing
                  block</a>. Its final position is determined by the values of <code>top</code>, <code>right</code>,
                <code>bottom</code>, and <code>left</code>.</p>
              <p>This value creates a new <a
                  href="/en-US/docs/Web/CSS/CSS_Positioning/Understanding_z_index/The_stacking_context">stacking
                context</a> when the value of <code>z-index</code> is not <code>auto</code>. The margins of absolutely
                positioned boxes do not <a
                    href="/en-US/docs/Web/CSS/CSS_Box_Model/Mastering_margin_collapsing">collapse</a> with other margins.
              </p>
            </dd>
            <dt id="fixed"><code>fixed</code></dt>
            <dd>
              <p>The element is removed from the normal document flow, and no space is created for the element in the page
                layout. It is positioned relative to the initial <a href="/en-US/docs/Web/CSS/Containing_block">containing
                  block</a> established by the <a href="/en-US/docs/Glossary/Viewport">viewport</a>, except when one of
                its ancestors has a <code>transform</code>, <code>perspective</code>, or <code>filter</code> property set
                to something other than <code>none</code> (see the <a
                    href="https://www.w3.org/TR/css-transforms-1/#propdef-transform" class="external" target="_blank">CSS
                  Transforms Spec</a>), or the <a href="/en-US/docs/Web/CSS/will-change"><code>will-change</code></a>
                property is set to <code>transform</code>, in which case that ancestor behaves as the containing block.
                (Note that there are browser inconsistencies with <code>perspective</code> and <code>filter</code>
                contributing to containing block formation.) Its final position is determined by the values of
                <code>top</code>, <code>right</code>, <code>bottom</code>, and <code>left</code>.</p>
              <p>This value always creates a new <a
                  href="/en-US/docs/Web/CSS/CSS_Positioning/Understanding_z_index/The_stacking_context">stacking
                context</a>. In printed documents, the element is placed in the same position on <em>every page</em>.</p>
            </dd>
            <dt id="sticky"><code>sticky</code></dt>
            <dd>
              <p>The element is positioned according to the normal flow of the document, and then offset relative to its
                <em>nearest scrolling ancestor</em> and <a href="/en-US/docs/Web/CSS/Containing_block">containing
                  block</a> (nearest block-level ancestor), including table-related elements, based on the values of
                <code>top</code>, <code>right</code>, <code>bottom</code>, and <code>left</code>. The offset does not
                affect the position of any other elements.</p>
              <p>This value always creates a new <a
                  href="/en-US/docs/Web/CSS/CSS_Positioning/Understanding_z_index/The_stacking_context">stacking
                context</a>. Note that a sticky element "sticks" to its nearest ancestor that has a "scrolling mechanism"
                (created when <code>overflow</code> is <code>hidden</code>, <code>scroll</code>, <code>auto</code>, or
                <code>overlay</code>), even if that ancestor isn't the nearest actually scrolling ancestor.</p>
            </dd>
          </dl>
        </div>
