      <section aria-labelledby="accessibility_concerns"><h2 id="accessibility_concerns"><a href="#accessibility_concerns">Accessibility
        concerns</a></h2>
        <div class="section-content"><p>Ensure that elements positioned with an <code>absolute</code> or
          <code>fixed</code> value do not obscure other content when the page is zoomed to increase text size.</p>
