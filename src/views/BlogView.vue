        <div class="section-content"><p>Sticky positioning can be thought of as a hybrid of relative and fixed positioning
          when its nearest scrolling ancestor is the viewport. A stickily positioned element is treated as relatively
          positioned until it crosses a specified threshold, at which point it is treated as fixed until it reaches the
          boundary of its parent. For example:</p>
          <div class="code-example"><pre class="brush: css notranslate"><code><span
