      <section aria-labelledby="types_of_positioning"><h3 id="types_of_positioning"><a href="#types_of_positioning">Types
        of positioning</a></h3>
        <div class="section-content">
          <ul>
            <li>A <strong>positioned element</strong> is an element whose <a href="/en-US/docs/Web/CSS/computed_value">computed</a>
              <code>position</code> value is either <code>relative</code>, <code>absolute</code>, <code>fixed</code>, or
              <code>sticky</code>. (In other words, it's anything except <code>static</code>.)
            </li>
            <li>A <strong>relatively positioned element</strong> is an element whose <a
                href="/en-US/docs/Web/CSS/computed_value">computed</a> <code>position</code> value is
              <code>relative</code>. The <a href="/en-US/docs/Web/CSS/top"><code>top</code></a> and <a
                  href="/en-US/docs/Web/CSS/bottom"><code>bottom</code></a> properties specify the vertical offset from
              its normal position; the <a href="/en-US/docs/Web/CSS/left"><code>left</code></a> and <a
                  href="/en-US/docs/Web/CSS/right"><code>right</code></a> properties specify the horizontal offset.
            </li>
            <li>An <strong>absolutely positioned element</strong> is an element whose <a
                href="/en-US/docs/Web/CSS/computed_value">computed</a> <code>position</code> value is
              <code>absolute</code> or <code>fixed</code>. The <a href="/en-US/docs/Web/CSS/top"><code>top</code></a>, <a
                  href="/en-US/docs/Web/CSS/right"><code>right</code></a>, <a href="/en-US/docs/Web/CSS/bottom"><code>bottom</code></a>,
              and <a href="/en-US/docs/Web/CSS/left"><code>left</code></a> properties specify offsets from the edges of
              the element's <a href="/en-US/docs/Web/CSS/Containing_block">containing block</a>. (The containing block is
              the ancestor relative to which the element is positioned.) If the element has margins, they are added to the
              offset. The element establishes a new <a href="/en-US/docs/Web/Guide/CSS/Block_formatting_context">block
                formatting context</a> (BFC) for its contents.
            </li>
            <li>A <strong>stickily positioned element</strong> is an element whose <a
                href="/en-US/docs/Web/CSS/computed_value">computed</a> <code>position</code> value is <code>sticky</code>.
              It's treated as relatively positioned until its <a href="/en-US/docs/Web/CSS/Containing_block">containing
                block</a> crosses a specified threshold (such as setting <a
                  href="/en-US/docs/Web/CSS/top"><code>top</code></a> to value other than auto) within its flow root (or
              the container it scrolls within), at which point it is treated as "stuck" until meeting the opposite edge of
              its <a href="/en-US/docs/Web/CSS/Containing_block">containing block</a>.
            </li>
          </ul>
          <p>Most of the time, absolutely positioned elements that have <a
              href="/en-US/docs/Web/CSS/height"><code>height</code></a> and <a href="/en-US/docs/Web/CSS/width"><code>width</code></a>
            set to <code>auto</code> are sized so as to fit their contents. However, non-<a
                href="/en-US/docs/Web/CSS/Replaced_element">replaced</a>, absolutely positioned elements can be made to
            fill the available vertical space by specifying both <a href="/en-US/docs/Web/CSS/top"><code>top</code></a>
            and <a href="/en-US/docs/Web/CSS/bottom"><code>bottom</code></a> and leaving <a
                href="/en-US/docs/Web/CSS/height"><code>height</code></a> unspecified (that is, <code>auto</code>). They
            can likewise be made to fill the available horizontal space by specifying both <a
                href="/en-US/docs/Web/CSS/left"><code>left</code></a> and <a
                href="/en-US/docs/Web/CSS/right"><code>right</code></a> and leaving <a
                href="/en-US/docs/Web/CSS/width"><code>width</code></a> as <code>auto</code>.</p>
          <p>Except for the case just described (of absolutely positioned elements filling the available space):</p>
          <ul>
            <li>If both <code>top</code> and <code>bottom</code> are specified (technically, not <code>auto</code>),
              <code>top</code> wins.
            </li>
            <li>If both <code>left</code> and <code>right</code> are specified, <code>left</code> wins when <a
                href="/en-US/docs/Web/CSS/direction"><code>direction</code></a> is <code>ltr</code> (English, horizontal
              Japanese, etc.) and <code>right</code> wins when <a
                  href="/en-US/docs/Web/CSS/direction"><code>direction</code></a> is <code>rtl</code> (Persian, Arabic,
              Hebrew, etc.).
            </li>
          </ul>
        </div>
