      <section aria-labelledby="performance_accessibility"><h3 id="performance_accessibility"><a
          href="#performance_accessibility">Performance &amp; Accessibility</a></h3>
        <div class="section-content"><p>Scrolling elements containing <code>fixed</code> or <code>sticky</code> content
          can cause performance and accessibility issues. As a user scrolls, the browser must repaint the sticky or fixed
          content in a new location. Depending on the content needing to be repainted, the browser performance, and the
          device's processing speed, the browser may not be able to manage repaints at 60 fps, causing accessibility
          concerns for people with sensitivities and jank for everyone. One solution is to add <a
              href="/en-US/docs/Web/CSS/will-change"><code>will-change: transform</code></a> to the positioned elements to
          render the element in its own layer, improving repaint speed and therefore improving performance and
          accessibility.</p></div>
